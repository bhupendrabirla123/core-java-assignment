package com.kaizen.Encapsulation;

public class Encapsulate {
	
	private int Roll;
	private String Name;
	
	
	public void setRollNo(int RollNo) {
		Roll = RollNo;
	}
	public void Setname(String Name) {
		this.Name = Name;
	}
	public int getRollNo() {
		return Roll;
	}
	public String getName() {
		return Name;
	}
	
}
